;-----------------------------------------------------------------------------
; Draw Image
;
; 	Data is split between even and odd using ptrAA and ptrAB
;-----------------------------------------------------------------------------
.proc drawImage
    sta     CLR80COL        ; Use RAMWRT for aux mem

    ldx 	imageY
loopY:
    lda     lineOffset,x
    clc
    adc 	imageX
    sta     screenPtr0
    lda     linePage,x
    sta     screenPtr1

    lda 	#8
    sta 	lineCount
loop8:

    ldy 	#0
loopX:
    sta     RAMWRTON  			; aux  
    lda		(ptrAA0),y
    sta 	(screenPtr0),y
    sta     RAMWRTOFF  			; main  
    lda		(ptrAB0),y
    sta 	(screenPtr0),y
    iny
    cpy 	imageWidth
    bne 	loopX

    ; increment pointers

    clc
    lda 	imageWidth
    adc 	ptrAA0
   	sta 	ptrAA0
   	lda 	#0
   	adc 	ptrAA1
   	sta 	ptrAA1

    clc
    lda 	imageWidth
    adc 	ptrAB0
   	sta 	ptrAB0
   	lda 	#0
   	adc 	ptrAB1
   	sta 	ptrAB1

    clc
   	lda     screenPtr1
    adc     #4
    sta     screenPtr1

    dec 	lineCount
    bne 	loop8

    inx
    cpx 	#8 			; 64/8
    bne 	loopY

    rts

lineCount: 	.byte 	0

.endproc

;-----------------------------------------------------------------------------
; Init
;-----------------------------------------------------------------------------
.proc init

    ; Set ctrl-y vector
    lda     #$4c        ; JMP
    sta     $3f8
    lda     #<quit
    sta     $3f9
    lda     #>quit
    sta     $3fa

    ; Set up text screen
    jsr     $c300       ; 80 column mode
    jsr     HOME        ; clear screen
    lda     #23         ; put cursor on last line
    sta     CV
    jsr     VTAB

    ; init DHGR (monochrome)

    sta     MIXCLR
    sta     HIRES
    sta     TXTCLR
    sta     LOWSCR
    ldx     #2
:
    sta     SET80COL
    sta     SET80VID 
    sta     CLR80VID
    sta     DHIRESON
    sta     DHIRESOFF
    sta     SET80VID 
    sta     DHIRESON
    dex
    bne     :-
    sta     MIXSET
    rts
.endproc

;-----------------------------------------------------------------------------
; Monitor
;
;  Exit to monitor
;-----------------------------------------------------------------------------
.proc monitor

    jsr    inline_print
    StringCR "Enter ctrl-y to quit to ProDos"

    jmp     MONZ        ; enter monitor

.endproc

;-----------------------------------------------------------------------------
; Quit
;
;   Exit to ProDos
;-----------------------------------------------------------------------------
.proc quit

    jsr     MLI
    .byte   CMD_QUIT
    .word   quit_params

quit_params:
    .byte   4               ; 4 parameters
    .byte   0               ; 0 is the only quit type
    .word   0               ; Reserved pointer for future use (what future?)
    .byte   0               ; Reserved byte for future use (what future?)
    .word   0               ; Reserved pointer for future use (what future?)

.endproc

;-----------------------------------------------------------------------------
; Clear screen
;-----------------------------------------------------------------------------

.proc clearScreen
    sta     CLR80COL        ; Use RAMWRT for aux mem

    lda     #$00
    sta     screenPtr0
    lda     #$20
    sta     screenPtr1

loop:
    ldy     #0
    lda     #0
    sta     RAMWRTON	; aux mem  
:
	sta     (screenPtr0),y
	iny
	bne     :-    
	sta     RAMWRTOFF	; main mem
:
    sta     (screenPtr0),y
    iny
    bne     :-    
 
 	inc     screenPtr1
    lda     #$40
    cmp     screenPtr1
    bne     loop
    rts
.endproc

